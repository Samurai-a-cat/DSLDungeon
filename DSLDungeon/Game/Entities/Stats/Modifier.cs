namespace DSLDungeon.Game.Entities.Stats;

/// <summary>
/// Тип модификатора определяет порядок применения в формуле.
/// Критически важно для Path-of-Exile-подобных вычислений.
/// </summary>
public enum ModifierType
{
    // === ФАЗА 1: Базовое значение ===
    Base,           // Базовый плоский урон оружия/способности
    BaseMultiplier, // Множитель базы (например, "урон увеличен на 50%")

    // === ФАЗА 2: Добавочный урон от статов ===
    Added,          // Добавочный плоский урон (от силы, ловкости, etc)
    AddedMultiplier,// Множитель добавочного урона

    // === ФАЗА 3: Глобальные множители ===
    More,           // Мультипликативный "more" (1.1 * 1.1 = 1.21)
    Less,           // Мультипликативный "less" (0.9 * 0.9 = 0.81)
    
    // === ФАЗА 4: Финальные модификаторы ===
    FinalAdded,     // Плоский урон после всех множителей
    FinalMultiplier,// Финальный множитель (например, удар в спину +30%)
}

/// <summary>
/// Источник модификатора — нужен для отслеживания и удаления
/// </summary>
public enum ModifierSource
{
    BaseStats,      // Базовые статы персонажа
    Equipment,      // Экипировка
    Buff,           // Положительный эффект
    Debuff,         // Отрицательный эффект
    PassiveSkill,   // Пассивное умение/талант
    Environmental,  // Окружение (высота, освещение)
    Impulse,        // Импульс (бонус после движения)
    Backstab,       // Удар в спину
    Combo,          // Комбо-система
}